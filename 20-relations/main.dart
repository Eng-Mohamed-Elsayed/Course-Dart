void main() {
  // * Grater Then
  print('Grater Then 5 > 2 : ${5 > 2} '); // true

  // * Less Then
  print('Less Then 3 < 2  : ${3 < 2}'); // false

  // * Grater Then Or Equal
  print('Grater Then Or Equal 5 >= 5  : ${5 >= 5} '); // F || T = T

  // * Less Then Or Equal
  print('Less Then Or Equal 3 <= 3 : ${3 <= 3} '); // F || T = T

  // * Is Equal To
  print('Is Equal to 20 == 20 : ${20 == 20}'); // true

  // * Is Not  Equal To
  print('Is Not  Equal to 20 != 30 : ${20 != 30}'); // true
}
