void main() {
  int a = 5, b = 10;

  // * Addition
  print('Addition : $a + $b = ${a + b}');

  // * subtraction
  print('subtraction : $a - $b = ${a - b}');

  // * Multiply
  print('Multiply : $a * $b = ${a * b}');

  // * Division
  print('Division : $a / $b = ${a / b}');

  // * Division return integer
  print('Division : $a / $b = ${a ~/ b}');

  // * Modulus
  print('Modulus : $a % $b = ${a % b}');

  // * unary Minus
  print('Revers ths sign of the expression : $a = ${-a}');
}
